#include <iostream>

int main(int argc, char const *argv[])
{
    int x(3), y(9);

    std::printf("y - x = %d \n", y - x);
    std::printf("y * x = %d \n", y * x);
    std::printf("y / x = %d \n", y / x);
    std::printf("y + x = %d \n", y + x);
    std::printf("y mod x = %d \n", y % x);

    return 0;
}
